// Buscar um estudante pelo nome e retornar a situação dele em cada matéria:
const studentStatus = (studentName, allStudents) => {
  const foundStudent = allStudents.find((student) => student.nome === studentName);
  const statuses = foundStudent.materias.map((subject) => (subject.nota < 60) ? `${subject.name}: Reprovado(a)` : `${subject.name}: Aprovado(a)`);
  return statuses;
};
